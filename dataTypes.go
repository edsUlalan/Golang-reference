package main
import ("fmt")

func main() {
  var a bool = true // Boolean
  var b int = 5 // Integer
  var c float32 = 3.14 // Floatiing point number
  var d string = "Hi!" // string

  fmt.Println("Boolean: ", a)
  fmt.Println("Integer: ", b)
  fmt.Println("Float: ", c)
  fmt.Println("String: ", d)
}

